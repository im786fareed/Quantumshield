const LS_COUNTRY = 'qs_shield_country';
const LS_OVERRIDE = 'qs_shield_manual_override';

/* ─── Auto-detect country from browser locale ─── */
function detectBrowserCountry(): string {
  if (typeof navigator === 'undefined') return 'IN';
  const lang = navigator.language || 'en-IN';
  const parts = lang.split('-');
  if (parts.length >= 2) return parts[parts.length - 1].toUpperCase();
  // Fallback mapping for single-part locales
  const localeMap: Record<string, string> = {
    hi: 'IN', en: 'IN', ar: 'SA', zh: 'CN', fr: 'FR',
    de: 'DE', es: 'ES', pt: 'BR', ja: 'JP', ko: 'KR',
    ru: 'RU', it: 'IT', nl: 'NL', tr: 'TR', pl: 'PL',
    sv: 'SE', no: 'NO', id: 'ID', ms: 'MY', th: 'TH',
  };
  return localeMap[parts[0]] || 'IN';
}

/* ─── Format phone for tel: link ─── */
function telLink(num: string) {
  return `tel:${num.replace(/[^0-9+]/g, '')}`;
}

