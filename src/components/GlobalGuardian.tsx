import globalSafetyData from '@/data/global-safety.json';

/* ─── Types ─── */
interface Country {
  code: string; name: string; flag: string; region: string;
  cyber_hotline: string; cyber_portal: string;
  police: string; ambulance: string; fire: string;
  cyber_agency: string; report_url: string;
  top_scams: string[]; scam_alert: string; languages: string[];
}

